/*
 * uart.c - serial drivers for CH32V208 door lock board
 *
 * USART1: PA9/PA10  debug CH340C, initialized by USART_Printf_Init()
 * USART2: PA2/PA3   RFID, 115200
 * USART3: PB10/PB11 voice module, 9600
 * UART4 : PC10/PC11 AS608 fingerprint, 115200
 */
#include "debug.h"

void Uart4_Init(void)
{
    GPIO_InitTypeDef GPIO_InitStructure;
    USART_InitTypeDef USART_InitStructure;
    NVIC_InitTypeDef NVIC_InitStructure;

    RCC_APB1PeriphClockCmd(RCC_APB1Periph_UART4, ENABLE);
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC | RCC_APB2Periph_AFIO, ENABLE);

    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;
    GPIO_Init(GPIOC, &GPIO_InitStructure);

    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_11;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;
    GPIO_Init(GPIOC, &GPIO_InitStructure);

    USART_InitStructure.USART_BaudRate = 115200;
    USART_InitStructure.USART_WordLength = USART_WordLength_8b;
    USART_InitStructure.USART_StopBits = USART_StopBits_1;
    USART_InitStructure.USART_Parity = USART_Parity_No;
    USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;
    USART_InitStructure.USART_Mode = USART_Mode_Tx | USART_Mode_Rx;
    USART_Init(UART4, &USART_InitStructure);

    USART_ITConfig(UART4, USART_IT_RXNE, ENABLE);
    NVIC_InitStructure.NVIC_IRQChannel = UART4_IRQn;
    NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 1;
    NVIC_InitStructure.NVIC_IRQChannelSubPriority = 1;
    NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
    NVIC_Init(&NVIC_InitStructure);

    USART_Cmd(UART4, ENABLE);
    USART_ClearFlag(UART4, USART_FLAG_TC);
}

void Uart7_Init(void)
{
    Uart4_Init();
}

void usart1_send_string(u8 *string, u8 len)
{
    u8 i;

    for(i = 0; i < len; i++)
    {
        USART_SendData(USART1, string[i]);
        while(USART_GetFlagStatus(USART1, USART_FLAG_TC) == RESET);
    }
}

void uart4_send_string(u8 *string, u8 len)
{
    u8 i;

    for(i = 0; i < len; i++)
    {
        USART_SendData(UART4, string[i]);
        while(USART_GetFlagStatus(UART4, USART_FLAG_TC) == RESET);
    }
    usart1_send_string(string, len);
}

void uart7_send_string(u8 *string, u8 len)
{
    uart4_send_string(string, len);
}

